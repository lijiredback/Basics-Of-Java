public class Constant {
    public static void main(String[] args) {
        System.out.println("Hello"); // 字符串常量
        System.out.println(100); // 整数常量
        System.out.println(10.1); // 小数常量
        System.out.println(true); // 布尔常量
        System.out.println('A'); // 字符常量
        // 空常量：null   无法打印
    }
}